#include<iostream>
using namespace std;
int main()
{
	/*i=i+by在c++中可以用组合赋值运算符+=这样表达：i+=by*/
	/*+=将两个操作数相加，并将结果赋给左边的操作数，左边的操作数可以是变量、数组元素、
	结构成员、通过对指针解除引用来标识的数据*/
	int k = 5;
	k += 3;/*将5+3的结果8赋给右边的操作数k*/
	int* pa = new int[10];/*指针pa指向pa[0]*/
	pa[4] = 12;
	pa[4] += 6;/*pa[4]现在等于18*/
	*(pa + 4) += 7;/**/
	pa += 2;/**/
	


	return 0;
};